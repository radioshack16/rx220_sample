//------------------------------------------------------------
// bit 4 to hex char 
//------------------------------------------------------------
unsigned char   b4toc(unsigned int d)
{
    if (d<10) {
        return '0'+d;
    } else if (d<16) {
        return 'A'+(d-10);
    } else {
        return 'F';
    }
}
